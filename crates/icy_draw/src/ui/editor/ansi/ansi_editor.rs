    /// Get status bar information for this editor
    pub fn status_info(&self) -> AnsiStatusInfo {
        let tool_hint = self.compute_tool_hint();
        let mut screen = self.screen.lock();
        let state = screen
            .as_any_mut()
            .downcast_mut::<EditState>()
            .expect("AnsiEditor screen should always be EditState");
        let buffer = state.get_buffer();
        let caret = state.get_caret();
        let format_mode = state.get_format_mode();

        // Get font info based on format mode
        let (font_name, current_font_slot, slot_fonts) = if format_mode == icy_engine_edit::FormatMode::XBinExtended {
            let slot0 = buffer.font(0).map(|f| f.name().to_string());
            let slot1 = buffer.font(1).map(|f| f.name().to_string());
            let current_slot = caret.font_page().min(1);
            (
                slot0.clone().or(slot1.clone()).unwrap_or_else(|| "Unknown".to_string()),
                current_slot,
                Some([slot0, slot1]),
            )
        } else {
            // Get font for current slot, falling back to slot 0 if not found
            let font_page = caret.font_page();
            let font_name = buffer
                .font(font_page)
                .or_else(|| buffer.font(0))
                .map(|f| f.name().to_string())
                .unwrap_or_else(|| "Unknown".to_string());
            (font_name, font_page, None)
        };

        // Get cursor position only if the current tool shows caret
        let cursor_position = if self.current_tool.show_caret() { Some((caret.x, caret.y)) } else { None };

        // Get selection range if there is a selection
        let selection_range = state.selection().map(|sel| {
            let min = sel.min();
            let max = sel.max();
            (min.x, min.y, max.x, max.y)
        });

        // Get ice colors and letter spacing from buffer
        let ice_colors = buffer.ice_mode.has_high_bg_colors();
        let letter_spacing = buffer.use_letter_spacing();
        let use_aspect_ratio = buffer.use_aspect_ratio();

        AnsiStatusInfo {
            cursor_position,
            selection_range,
            buffer_size: (buffer.width(), buffer.height()),
            font_name,
            ice_colors,
            letter_spacing,
            use_aspect_ratio,
            format_mode,
            current_font_slot: current_font_slot as usize,
            slot_fonts,
            tool_hint,
        }
    }

    /// Build a one-line discoverability hint for the active tool / brush mode.
    ///
    /// Surfaced in the status bar so users discover (e.g.) that the Pencil tool
    /// in `Char` mode is what paints the full block (`█`) — see issue #153.
    /// Reads only `current_tool` + the shared `BrushSettings`; never touches
    /// `EditState`, so it's safe to call without holding the screen lock.
    fn compute_tool_hint(&self) -> Option<String> {
        use tools::ToolId;
        let brush = *self.top_toolbar.brush.read();
        let mode = brush_mode_label(brush.primary, brush.paint_char);
        let hint = match self.current_tool.id() {
            ToolId::Click => crate::fl!("tool-hint-click"),
            ToolId::Select => crate::fl!("tool-hint-select"),
            ToolId::Pencil => crate::fl!("tool-hint-pencil", brush = mode),
            ToolId::Line => crate::fl!("tool-hint-line", brush = mode),
            ToolId::RectangleOutline => crate::fl!("tool-hint-rectangle", brush = mode),
            ToolId::RectangleFilled => crate::fl!("tool-hint-filled_rectangle", brush = mode),
            ToolId::EllipseOutline => crate::fl!("tool-hint-ellipse", brush = mode),
            ToolId::EllipseFilled => crate::fl!("tool-hint-filled_ellipse", brush = mode),
            ToolId::Pipette => crate::fl!("tool-hint-pipette"),
            ToolId::Fill => crate::fl!("tool-hint-fill", brush = mode),
            ToolId::Font => crate::fl!("tool-hint-font"),
            ToolId::Tag => crate::fl!("tool-hint-tag"),
            ToolId::Paste => crate::fl!("tool-hint-paste"),
        };
        Some(hint)
    }

