lazy_static::lazy_static! {
    pub static ref UNICODE_TO_ATARI: std::collections::HashMap<char, char> = {
        let mut res = std::collections::HashMap::new();
        (0..128).for_each(|a: u8| {
            res.insert(ATARI_TO_UNICODE[a as usize], a as char);
        });
        res
    };

    pub static ref UNICODE_TO_VIEWDATA: std::collections::HashMap<char,char> = {
        let mut res = std::collections::HashMap::new();
        (0..=255).for_each(|a: u8| {
            res.insert(VIEWDATA_TO_UNICODE[a as usize], a as char);
        });
        res
    };
}

